// ─────────────────────────────────────────────
// 학과명 → 권장과목 검색
// ─────────────────────────────────────────────
export function findCriteria(univ, dept) {
  if (!univ || !kcueData[univ]) return null;
  const list = kcueData[univ];
  const deptKey = String(dept || '').replace(/\s+/g,'').toLowerCase();

  // 1단계: 정확 일치
  let hit = list.find(r => {
    const targets = (r.target || '').split(/[,·、]/).map(s => s.replace(/\s+/g,'').toLowerCase());
    return targets.some(t => t && (t === deptKey || deptKey.includes(t) || t.includes(deptKey)));
  });

  // 2단계: 그룹 매칭 (학과명이 안 잡히면 계열로)
  if (!hit) {
    hit = list.find(r => {
      const gk = String(r.group || '').replace(/\s+/g,'').toLowerCase();
      return gk && (gk === deptKey || deptKey.includes(gk) || gk.includes(deptKey));
    });
  }

  // 3단계: 부분 일치
  if (!hit) {
    hit = list.find(r => (r.target || '').includes(dept) || (r.group || '').includes(dept));
  }

  return hit || null;
}

// ─────────────────────────────────────────────
// 학생 이수 과목 vs 권장과목 충족률 계산
// ─────────────────────────────────────────────
// grades: parser.js의 추출 결과 (subject 필드 활용)
// achievementSubjects: 진로선택 과목 (성취도 ABC)
// returns: { rate: 0-100, matched: [...], missing: [...], note: '...' }
export function calcCreditRate(criteria, grades, achievementSubjects) {
  if (!criteria || (!criteria.core && !criteria.recommend)) {
    return {
      rate: 0,
      matched: [],
      missing: [],
      note: '권장과목이 별도로 지정되지 않은 대학·학과입니다. 진로·적성에 따라 자율 선택을 권장합니다.'
    };
  }

  const allTaken = new Set();
  (grades || []).forEach(g => g.subject && allTaken.add(normalizeSubject(g.subject)));
  (achievementSubjects || []).forEach(a => a.subject && allTaken.add(normalizeSubject(a.subject)));

  // 권장 과목 텍스트에서 과목명 추출
  const required = extractSubjects(criteria.core || '');
  const recommended = extractSubjects(criteria.recommend || '');
  const all = [...new Set([...required, ...recommended])];

  if (all.length === 0) {
    return {
      rate: 0,
      matched: [],
      missing: [],
      note: criteria.core || criteria.recommend || '권장과목 미명시'
    };
  }

  const matched = [];
  const missing = [];
  all.forEach(s => {
    const norm = normalizeSubject(s);
    let hit = false;
    for (const taken of allTaken) {
      if (taken.includes(norm) || norm.includes(taken)) { hit = true; break; }
    }
    if (hit) matched.push(s);
    else missing.push(s);
  });

  const rate = Math.round((matched.length / all.length) * 100);

  return {
    rate,
    matched,
    missing,
    required,
    recommended,
    note: criteria.core ? `핵심: ${criteria.core}` : `권장: ${criteria.recommend}`
  };
}

// ─────────────────────────────────────────────
// 보조 함수
// ─────────────────────────────────────────────
function normalizeSubject(s) {
  return String(s)
    .replace(/\s+/g,'')
    .replace(/[ⅠⅡⅢⅣⅤ]/g, m => ({Ⅰ:'1',Ⅱ:'2',Ⅲ:'3',Ⅳ:'4',Ⅴ:'5'}[m]))
    .replace(/[()]/g,'')
    .toLowerCase();
}

function extractSubjects(text) {
  if (!text) return [];
  // 한국어 교과목명 패턴 추출
  // 쉼표/중점/'및'/'또는' 기준 분리
  const cleaned = text
    .replace(/\[.*?\]|\(.*?\)/g, '')  // 괄호 안 설명 제거
    .replace(/(과목 위계를?|선택|이수|중\s*\d+과목|이상|포함)/g, ',')
    .replace(/\s*(및|또는|그리고)\s*/g, ',');

  const subjects = cleaned
    .split(/[,·、]/)
    .map(s => s.trim())
    .filter(s => {
      if (!s || s.length < 2 || s.length > 20) return false;
      // 일반어 제외
      if (/^(또는|및|와|과|중|등|이상|이하|미만|초과|포함|위계|고려|이수|선택|진로|적성|계열|구분|없이|자신의|자율|자유|핵심|권장|반영|일반선택|진로선택|융합선택)$/.test(s)) return false;
      // 한글 1자라도 포함되어야 함
      if (!/[가-힣]/.test(s)) return false;
      return true;
    });

  return [...new Set(subjects)];
}

// ─────────────────────────────────────────────
// 대학 + 학과별 종합 진단
// ─────────────────────────────────────────────
export function diagnoseUnivFit(univ, dept, grades, achievementSubjects) {
  const criteria = findCriteria(univ, dept);
  if (!criteria) {
    return {
      univ, dept,
      hasCriteria: false,
      note: `${univ}의 ${dept} 권장과목 데이터를 찾지 못했습니다. 대학 홈페이지를 참고하세요.`
    };
  }

  const fit = calcCreditRate(criteria, grades, achievementSubjects);

  return {
    univ, dept,
    hasCriteria: true,
    criteria: {
      target: criteria.target,
      group: criteria.group,
      core: criteria.core,
      recommend: criteria.recommend
    },
    ...fit
  };
}

